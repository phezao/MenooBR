<template>
  <div class="w-72">
    <Listbox>
      <div class="relative mt-1">
        <ListboxButton
          class="relative w-full py-2 pl-3 pr-10 text-left
          bg-gray-100 rounded-lg h-14 cursor-default
          focus:outline-none focus-visible:ring-2 focus-visible:ring-opacity-75
          focus-visible:ring-white focus-visible:ring-offset-orange-300
          focus-visible:ring-offset-2 focus-visible:border-indigo-500 sm:text-sm"
        >
          <span class="block truncate" > {{ optionsSelected.value }}</span>
          <span class="absolute inset-y-0 right-0 flex items-center pr-2 pointer-events-none" >
            <SelectorIcon class="w-5 h-5 text-gray-400" aria-hidden="true" />
          </span>
        </ListboxButton>

        <transition leave-active-class="transition duration-100 ease-in"
          leave-from-class="opacity-100"
          leave-to-class="opacity-0"
        >
          <ListboxOptions
            class="absolute w-full py-1 mt-1 overflow-auto text-base
            bg-white rounded-md shadow-lg max-h-60 ring-1 ring-black
            ring-opacity-5 focus:outline-none sm:text-sm"
          >
            <ListboxOption
              v-slot="{ active, selected }"
              v-for="person in people"
              :key="person.value"
              :value="person"
              as="template"
            >
              <li :class="[
                active ? 'text-amber-900 bg-amber-100' : 'text-gray-900',
                'cursor-default select-none relative py-2 pl-10 pr-4',]"
                @blur="updateSelectedOptions(person)"
              >
                <span :class="[selected ? 'font-medium' : 'font-normal', 'block truncate',]">{{ person.value }}</span>
                <span v-if= "selected" class="absolute inset-y-0 left-0 flex items-center pl-3 text-amber-600">
                  <CheckIcon class="w-5 h-5" aria-hidden="true" />
                </span>
              </li>
            </ListboxOption>
          </ListboxOptions>
       </transition>
      </div>
    </Listbox>
  </div>
</template>

<script>
import { reactive, defineComponent, toRefs } from 'vue'
import {
  Listbox,
  ListboxLabel,
  ListboxButton,
  ListboxOptions,
  ListboxOption,
} from '@headlessui/vue'
import { CheckIcon, SelectorIcon } from '@heroicons/vue/solid'

export default defineComponent({
  components: {
    Listbox,
    ListboxLabel,
    ListboxButton,
    ListboxOptions,
    ListboxOption,
    CheckIcon,
    SelectorIcon
  },
  setup(){
    const people = [
      { value: 'Wade Cooper' },
      { value: 'Arlene Mccoy' },
      { value: 'Devon Webb' },
      { value: 'Tom Cook' },
      { value: 'Tanya Fox' },
      { value: 'Hellen Schmidt' },
    ]
    const state = reactive({
      optionsSelected: []
    })

    const updateSelectedOptions = (option) => {
      const exist = state.optionsSelected.find(element => element.value === option.value)

      if (exist) {
        state.optionsSelected = state.optionsSelected.filter(element => element.value != option.value)
      } else {
        state.optionsSelected.push(option);
      }
    }

    return {
      ...toRefs(state),
      people,
      updateSelectedOptions
    }
  }
});
</script>
